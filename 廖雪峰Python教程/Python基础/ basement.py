# 数据类型和变量

# 转义字符 \
print('I\'m studing python');
print('I\'m \n studing python');

# 多行打印
print('''111
222
333''')

# 布尔值 and or not
print(True and True)
print(False or True)
print(not True)

# 空值 None
print(None);

# 定义变量
a = 1
print(a)

# 除法
# / 精确除法
b = 2;
print(b/1) # 2.0
# // 只取整数部分
print(b//3) # 0
# & 取余
print(b%3)  # 2